def test_projection_region_counts_and_variants_from_fixture():
    mod = _load_module()
    projection = mod["build_projection"]()
    fixture = json.loads(CANONICAL.read_text(encoding="utf-8"))
    by_id = {r["region_id"]: r for r in fixture["regions"]}

    assert projection["region_order"] == REGION_ORDER
    assert projection["clone_status"] == "capture_required"
    assert projection["exact_clone_claimed"] is False
    assert projection["fixture_sha256"] == EXPECTED_FIXTURE_SHA

    for rid in REGION_ORDER:
        proj_region = projection["regions"][rid]
        src = by_id[rid]
        assert proj_region["item_count"] == EXPECTED_COUNTS[rid]
        assert proj_region["item_count"] == len(src["items"])
        assert len(proj_region["items"]) == len(src["items"])
        # First/last item identity and order fidelity
        assert proj_region["items"][0]["item_id"] == src["items"][0]["item_id"]
        assert proj_region["items"][-1]["item_id"] == src["items"][-1]["item_id"]
        for a, b in zip(proj_region["items"], src["items"]):
            assert a["order"] == b["order"]
            assert a.get("text") == (b.get("text") if b.get("text") is not None else a.get("text"))
            if b.get("text") is not None:
                assert a["text"] == b["text"]
            if b.get("date_text") is not None:
                assert a["date_text"] == b["date_text"]
            if "href" in b and b["href"] is not None:
                assert a.get("href") == b["href"]
            if "same_origin" in b and b["same_origin"] is not None:
                assert a.get("same_origin") == b["same_origin"]

    util = projection["regions"]["utility_navigation"]
    assert util["variant_counts"]["desktop"] == 18
    assert util["variant_counts"]["hidden"] == 3


