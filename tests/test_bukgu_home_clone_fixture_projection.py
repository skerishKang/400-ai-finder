def test_static_build_includes_home_fixture(tmp_path):
    import importlib.util

    out = tmp_path / "pages"
    module_path = ROOT / "scripts" / "build_cloudflare_pages.py"
    spec = importlib.util.spec_from_file_location("build_cloudflare_pages", module_path)
    mod = importlib.util.module_from_spec(spec)
    assert spec.loader is not None
    spec.loader.exec_module(mod)
    mod.build(out_dir=str(out), mode="static")
    built = out / "static" / "bukgu-home-clone-fixture.js"
    assert built.is_file()
    assert built.read_bytes() == BROWSER.read_bytes()
    html = (out / "static" / "citizen-action-demo.html").read_text(encoding="utf-8")
    assert html.index("bukgu-home-clone-fixture.js") < html.index(
        "citizen-action-demo-canvas.js"
    )
